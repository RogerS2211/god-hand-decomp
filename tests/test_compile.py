class TestResolveSingleFileTargets:
    """Five happy paths + three error shapes from the authoritative
    catalog.  Uses synthetic ``CompileUnit`` and ``CarveState`` objects so
    we don't need a real build tree.
    """

    @staticmethod
    def _mkunit(rel: str, kind: str = "asm", root: Path | None = None) -> cm.CompileUnit:
        if root is None:
            root = cm.ROOT
        src = (root / rel).resolve()
        obj = (root / "build" / rel).with_suffix(".o").resolve()
        return cm.CompileUnit(
            src=src, obj=obj, kind=kind, rel=Path(rel)
        )

    @staticmethod
    def _mkcarve(tmp_path: Path, fragment_count: int, carves: dict[str, dict]
                 ) -> cm.CarveState:
        """Build a CarveState with ``fragment_count`` fragments plus one
        carved .s per entry in ``carves``.  ``carves[name]`` is
        ``{tu: Optional[str], size: int}``.
        """
        frag_sources = []
        for i in range(fragment_count):
            fp = tmp_path / f"build/asm/cod/000000.part{i}.s"
            fp.parent.mkdir(parents=True, exist_ok=True)
            fp.write_text(f"; fragment {i}\n")
            frag_sources.append(fp)
        carved_sources = {}
        for name in carves:
            cp = tmp_path / f"build/asm/nonmatching/{name}.s"
            cp.parent.mkdir(parents=True, exist_ok=True)
            cp.write_text(f"; carve {name}\n")
            carved_sources[name] = cp
        entries = [
            cm.CarveEntry(
                name=name,
                unit="asm/cod/000000",
                vaddr=0x100000,
                size=info["size"],
                tu=info.get("tu"),
            )
            for name, info in carves.items()
        ]
        return cm.CarveState(
            original_unit_src=(tmp_path / "asm/cod/000000.s").resolve(),
            original_unit_rel=Path("asm/cod/000000.s"),
            fragment_sources=frag_sources,
            carved_sources=carved_sources,
            entries=entries,
            lcf=tmp_path / "build/SLUS_215.03.lcf",
        )

    # ---- five happy paths ------------------------------------------------ #

    def test_direct_c_tu_match(self, tmp_path, monkeypatch):
        # Point compile.ROOT at the test workspace so the resolver's
        # logging `relative_to(ROOT)` calls don't blow up on tmp paths.
        monkeypatch.setattr(cm, "ROOT", tmp_path)
        (tmp_path / "src/cod").mkdir(parents=True)
        (tmp_path / "src/cod/000000.c").write_text("")
        u = self._mkunit("src/cod/000000.c", kind="c", root=tmp_path)
        log = cm.Logger(verbose=False)
        out = cm._resolve_single_file_targets(
            (tmp_path / "src/cod/000000.c").resolve(),
            carve=None,
            units=[u],
            log=log,
        )
        assert out == [u]

    def test_direct_noncarved_asm_match(self, tmp_path, monkeypatch):
        monkeypatch.setattr(cm, "ROOT", tmp_path)
        (tmp_path / "asm/data/cod").mkdir(parents=True)
        (tmp_path / "asm/data/cod/2BBF80.data.s").write_text("")
        u = self._mkunit("asm/data/cod/2BBF80.data.s", kind="asm", root=tmp_path)
        out = cm._resolve_single_file_targets(
            (tmp_path / "asm/data/cod/2BBF80.data.s").resolve(),
            carve=None,
            units=[u],
            log=cm.Logger(verbose=False),
        )
        assert out == [u]

    def test_direct_build_fragment_match(self, tmp_path, monkeypatch):
        monkeypatch.setattr(cm, "ROOT", tmp_path)
        carve = self._mkcarve(tmp_path, fragment_count=2, carves={})
        u0 = cm.CompileUnit(
            src=carve.fragment_sources[0],
            obj=carve.fragment_sources[0].with_suffix(".o"),
            kind="asm",
            rel=carve.fragment_sources[0].relative_to(tmp_path),
        )
        u1 = cm.CompileUnit(
            src=carve.fragment_sources[1],
            obj=carve.fragment_sources[1].with_suffix(".o"),
            kind="asm",
            rel=carve.fragment_sources[1].relative_to(tmp_path),
        )
        out = cm._resolve_single_file_targets(
            carve.fragment_sources[1].resolve(),
            carve=carve,
            units=[u0, u1],
            log=cm.Logger(verbose=False),
        )
        assert out == [u1]

    def test_carved_monolithic_expands_to_all_fragments(self, tmp_path, monkeypatch):
        monkeypatch.setattr(cm, "ROOT", tmp_path)
        """User names asm/cod/000000.s (the carved-out monolithic) — the
        resolver returns every fragment .o that replaces it.
        """
        # Place a real file at the monolithic source path so .resolve()
        # gives us a stable comparable Path.
        (tmp_path / "asm/cod").mkdir(parents=True)
        (tmp_path / "asm/cod/000000.s").write_text("; monolithic stub\n")

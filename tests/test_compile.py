class TestParseCarvedEntries:

    def test_empty_list_returns_empty(self):
        assert cm._parse_carved_entries(_mkcfg()) == []

    def test_entry_with_tu(self):
        cfg = _mkcfg(carved_funcs=[
            {"name": "func_00100000", "unit": "asm/cod/000000",
             "vaddr": "0x00100000", "size": 4,
             "tu": "src/cod/000000.c"}
        ])
        [entry] = cm._parse_carved_entries(cfg)
        assert entry.name == "func_00100000"
        assert entry.unit == "asm/cod/000000"
        assert entry.vaddr == 0x00100000
        assert entry.size == 4
        assert entry.tu == "src/cod/000000.c"

    def test_entry_without_tu(self):
        cfg = _mkcfg(carved_funcs=[
            {"name": "func_00100000", "unit": "asm/cod/000000",
             "vaddr": "0x00100000", "size": 4}
        ])
        [entry] = cm._parse_carved_entries(cfg)
        assert entry.tu is None

    def test_vaddr_accepts_int(self):
        cfg = _mkcfg(carved_funcs=[
            {"name": "f", "unit": "asm/cod/000000",
             "vaddr": 0x100000, "size": 4}
        ])
        [entry] = cm._parse_carved_entries(cfg)
        assert entry.vaddr == 0x100000

    def test_vaddr_accepts_decimal_string(self):
        cfg = _mkcfg(carved_funcs=[
            {"name": "f", "unit": "asm/cod/000000",
             "vaddr": "1048576", "size": 4}
        ])
        [entry] = cm._parse_carved_entries(cfg)
        assert entry.vaddr == 0x100000

    def test_doc_entries_filtered_via_underscore_name(self):
        # Documentation entries (name starts with `_`) are dropped by
        # Config.carved_funcs *before* _parse_carved_entries sees them.
        cfg = _mkcfg(carved_funcs=[
            {"name": "_doc", "unit": "asm/cod/000000",
             "vaddr": 0, "size": 0},
            {"name": "real", "unit": "asm/cod/000000",
             "vaddr": 0x100000, "size": 4},
        ])
        entries = cm._parse_carved_entries(cfg)
        assert [e.name for e in entries] == ["real"]

    def test_non_dict_entries_filtered(self):
        cfg = _mkcfg(carved_funcs=[
            "not a dict",
            {"name": "real", "unit": "asm/cod/000000",
             "vaddr": 0x100000, "size": 4},
        ])
        assert [e.name for e in cm._parse_carved_entries(cfg)] == ["real"]

    def test_malformed_missing_name_raises(self):
        cfg = _mkcfg(carved_funcs=[
            {"unit": "asm/cod/000000", "vaddr": 0, "size": 4}
        ])
        # No `name` key -> filtered out by Config.carved_funcs (empty
        # string), so the parsed list is just empty.
        assert cm._parse_carved_entries(cfg) == []

    def test_malformed_missing_unit_raises_keyerror(self):
        cfg = _mkcfg(carved_funcs=[
            {"name": "f", "vaddr": 0, "size": 4}
        ])
        with pytest.raises(KeyError):
            cm._parse_carved_entries(cfg)



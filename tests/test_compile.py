        cfg = _mkcfg(rels=[self._r207_entry()])
        [rel] = cfg.rels
        assert rel.name == "r207"
        assert rel.lcf.name == "r207.lcf"
        assert rel.lcf.is_absolute()
        assert rel.sha256_expected == self._R207_SHA
        assert rel.size_expected == 1720
        assert rel.build_path.name == "r207"
        assert rel.build_path.parent.name == "rel"
        assert [p.kind for p in rel.parts] == ["bin", "asm", "bin"]

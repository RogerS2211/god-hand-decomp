"""Unit tests for the pure-Python helpers inside ``compile.py``.

These tests lock in the early behaviours (carving, single-file
resolver, linker-script parsing) so later real-decomp churn can't
silently regress them.

Out of scope (deliberately):
  * Mocking the assembler / linker / ee-cc-wrap.  End-to-end build
    remains integration-tested via ``scripts/session_check.sh``.
  * Refactoring ``compile.py``.  If a helper is hard to test, the
    test contorts; this is documented coupling and a future task's
    problem.

Test inventory (one ``-k`` slice per helper):

    parse_carved_entries  — empty / with-tu / without-tu / malformed
    index_functions       — glabel/endlabel/nonmatching/alabel-skip/no-endlabel
    preamble_end_index    — finds `.section .text`, raises if missing
    split_monolithic      — middle-carve / pad-nop-swallow / not-present /
                            multi-carve order / overlap
    resolve_single_file_targets — 5 happy paths + 3 error shapes
    ld_script_objects     — order-preserving + dedup + carve-aware
"""
from __future__ import annotations

import json
import textwrap
from pathlib import Path

import pytest

import compile as cm



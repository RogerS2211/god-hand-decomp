        cfg = _mkcfg(rels=[
            {"_comment": "doc string"},
            {"name": "_skip_me", "lcf": "x"},   # leading-underscore name
            "not_a_dict",                        # non-dict
            self._r207_entry(),
        ])
        rels = cfg.rels
        assert len(rels) == 1
        assert rels[0].name == "r207"


class TestSplitMonolithic:

    def test_carve_middle_function_emits_two_fragments(self):
        fragments, carved, ranges = cm._split_monolithic(MONOLITHIC_3FN, ["beta"])
        assert len(fragments) == 2
        assert set(carved.keys()) == {"beta"}
        assert len(ranges) == 1
        assert ranges[0][0] == "beta"

    def test_carved_text_contains_glabel_endlabel_and_nonmatching(self):
        _frags, carved, _ranges = cm._split_monolithic(MONOLITHIC_3FN, ["beta"])
        body = carved["beta"]
        assert "glabel beta" in body
        assert "endlabel beta" in body
        assert "nonmatching beta, 0x8" in body
        assert ".align 3" in body

    def test_carved_preamble_uses_labels_inc_not_macro_inc(self):
        """Carve preamble swaps `.include "macro.inc"` for `labels.inc`
        because ee-as (the SCE assembler) rejects macro.inc's modern-gas
        quoted-escape syntax.  Fragments keep macro.inc (mipsel-as path).
        """
        _frags, carved, _ranges = cm._split_monolithic(MONOLITHIC_3FN, ["beta"])
        body = carved["beta"]
        assert '.include "labels.inc"' in body
        assert '.include "macro.inc"' not in body

    def test_fragments_keep_macro_inc(self):
        fragments, _carved, _ranges = cm._split_monolithic(MONOLITHIC_3FN, ["beta"])
        # Both fragments must carry the macro.inc include — fragment 0
        # naturally (it owns the original preamble), fragment 1 because
        # we re-prepend the preamble for standalone assembly.
        for f in fragments:
            assert '.include "macro.inc"' in f
            assert '.include "labels.inc"' not in f

    def test_carved_function_bytes_not_in_remaining_fragments(self):
        """The carved function's body must not leak into either fragment
        — that would link-duplicate the bytes and break retail equality.
        """
        fragments, _carved, _ranges = cm._split_monolithic(MONOLITHIC_3FN, ["beta"])
        for f in fragments:
            assert "glabel beta" not in f
            assert "endlabel beta" not in f

    def test_pad_nop_swallowed_into_previous_carve(self):
        """The trailing `nop` between endlabel beta and the next `.align 3`
        must travel with beta, not with fragment-1 — otherwise fragment-1's
        `.align 3` finds the cursor misaligned and emits stray bytes.
        """
        fragments, carved, _ranges = cm._split_monolithic(MONOLITHIC_3FN, ["beta"])
        body = carved["beta"]
        # The hex comment "10 00100010" marks the trailing-pad nop.
        assert "00100010" in body
        # The second fragment must start cleanly at the gamma `.align 3`,
        # not at the trailing nop.
        # gamma's `.align 3` must be the first non-preamble directive.
        frag1 = fragments[1]
        post_preamble = frag1.split(".section .text", 1)[1]
        # The first non-empty / non-comment instruction should not be a nop.
        assert "00100010" not in post_preamble

    def test_first_fragment_inherits_preamble_naturally(self):
        """When carving from non-first position, fragment 0 contains the
        original preamble exactly — no re-prepend.  Fragment 0's length
        equals the slice from start to first carve's block_start.
        """
        fragments, _carved, ranges = cm._split_monolithic(MONOLITHIC_3FN, ["beta"])
        # Fragment 0 must start with the original `.include "macro.inc"`.
        assert fragments[0].startswith('.include "macro.inc"')
        # And it must contain alpha but not beta or gamma.
        assert "glabel alpha" in fragments[0]
        assert "glabel beta" not in fragments[0]
        assert "glabel gamma" not in fragments[0]
        # Tail fragment contains gamma.
        assert "glabel gamma" in fragments[1]

    def test_multi_carve_order_independent(self):
        """Passing names out of source order still emits fragments in
        source order — the function re-sorts by carve start line.
        """
        fragments, carved, ranges = cm._split_monolithic(
            MONOLITHIC_3FN, ["gamma", "alpha"]
        )
        assert len(fragments) == 3
        assert [r[0] for r in ranges] == ["alpha", "gamma"]
        # Middle fragment carries beta in full.
        assert "glabel beta" in fragments[1]
        assert "glabel alpha" not in fragments[1]
        assert "glabel gamma" not in fragments[1]

    def test_function_not_in_monolithic_raises(self):
        with pytest.raises(cm.BuildError, match="not present in monolithic"):
            cm._split_monolithic(MONOLITHIC_3FN, ["does_not_exist"])

    def test_carve_first_function(self):
        """Carving the very first function exercises the cursor == 0
        branch (fragment 0 is empty/just preamble, no re-prepend).
        """
        fragments, carved, _ranges = cm._split_monolithic(MONOLITHIC_3FN, ["alpha"])
        assert len(fragments) == 2
        assert "glabel alpha" not in fragments[0]
        assert "glabel alpha" not in fragments[1]
        assert "glabel alpha" in carved["alpha"]

    def test_carve_last_function(self):
        """Carving the last function: trailing fragment is preamble-only
        (or near-empty) and must still be well-formed.
        """
        fragments, carved, _ranges = cm._split_monolithic(MONOLITHIC_3FN, ["gamma"])
        assert len(fragments) == 2
        # Final fragment is preamble + tail (likely no glabels).
        assert "glabel gamma" not in fragments[-1]
        assert "glabel gamma" in carved["gamma"]
        # Fragment 0 retains alpha and beta.
        assert "glabel alpha" in fragments[0]
        assert "glabel beta" in fragments[0]


# --------------------------------------------------------------------------- #
# _carve_unit_size_bytes
#
# The LCF location counter needs the carve unit's TRUE byte span (body +
# trailing nop pad), not just the function body size.  Without the
# trailing-pad bytes, a TU-integrated C body smaller than the carve
# unit shifts every subsequent function in .text earlier, drifting the
# ELF sha256.
# --------------------------------------------------------------------------- #


class TestCarveUnitSizeBytes:
    """Coverage for ``_carve_unit_size_bytes``.

    The function counts instruction lines in a carved-fragment .s text
    (the form spimdisasm emits with ``/* FILE_OFF VRAM HEX_BYTES */``
    comment prefixes) and multiplies by 4 for MIPS.
    """

    def test_alpha_body_with_one_nop_pad(self):
        """``alpha`` = 1-insn 4-B body + 1 trailing nop = 8 B carve unit."""
        _frags, carved, _ranges = cm._split_monolithic(
            MONOLITHIC_3FN, ["alpha"]
        )
        assert cm._carve_unit_size_bytes(carved["alpha"]) == 8

    def test_beta_body_with_one_nop_pad(self):
        """``beta`` = 2-insn 8-B body + 1 trailing nop = 12 B carve unit."""
        _frags, carved, _ranges = cm._split_monolithic(
            MONOLITHIC_3FN, ["beta"]
        )
        assert cm._carve_unit_size_bytes(carved["beta"]) == 12

    def test_gamma_body_no_trailing_pad(self):
        """``gamma`` = 1-insn 4-B body, no trailing pad = 4 B carve unit."""
        _frags, carved, _ranges = cm._split_monolithic(
            MONOLITHIC_3FN, ["gamma"]
        )
        assert cm._carve_unit_size_bytes(carved["gamma"]) == 4

    def test_empty_string_returns_zero(self):
        assert cm._carve_unit_size_bytes("") == 0

    def test_no_instruction_lines_returns_zero(self):
        """Pure directives + labels + blanks contribute no bytes."""
        text = (
            '.section .text, "ax"\n'
            ".align 3\n"
            "glabel foo\n"
            "endlabel foo\n"
        )
        assert cm._carve_unit_size_bytes(text) == 0

    def test_directives_dont_count(self):
        """Only instruction lines (with ``/* HEX HEX HEX */`` comment
        prefix) count.  Directives, labels, blanks, and nonmatching
        markers contribute zero."""
        text = (
            ".section .text, \"ax\"\n"
            ".align 3\n"
            "nonmatching foo, 0x4\n"
            "\n"
            "glabel foo\n"
            "    /* 0 00100000 0800E003 */  jr $ra\n"
            "endlabel foo\n"
        )
        assert cm._carve_unit_size_bytes(text) == 4

    def test_eight_byte_body_with_large_trailing_pad(self):
        """Mirror of ``func_00138BB8``'s shape: 2-insn 8-B body + 8
        trailing nops = 40-B carve unit.  This is the drift case that
        the LCF size fix addresses."""
        text = (
            ".section .text.foo, \"ax\"\n"
            ".align 3\n"
            "nonmatching foo, 0x8\n"
            "\n"
            "glabel foo\n"
            "    /* 0 00100000 0800E003 */  jr $ra\n"
            "    /* 4 00100004 00000000 */   nop\n"
            "endlabel foo\n"
            "    /* 8 00100008 00000000 */  nop\n"
            "    /* C 0010000C 00000000 */  nop\n"
            "    /* 10 00100010 00000000 */  nop\n"
            "    /* 14 00100014 00000000 */  nop\n"
            "    /* 18 00100018 00000000 */  nop\n"
            "    /* 1C 0010001C 00000000 */  nop\n"
            "    /* 20 00100020 00000000 */  nop\n"
            "    /* 24 00100024 00000000 */  nop\n"
        )
        assert cm._carve_unit_size_bytes(text) == 40



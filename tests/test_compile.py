def _mkcfg(**overrides) -> cm.Config:
    """Build a minimal ``Config`` for tests that only need a subset of
    fields.  Anything not in ``overrides`` is filled with empty defaults.
    """
    raw = {
        "tools": {},
        "include_dirs": [],
        "defines": [],
        "c_flags": [],
        "as_flags": [],
        "dvp_as_flags": [],
        "linker_scripts": [],
        "linker_flags": [],
        "asm_sources_glob": [],
        "c_sources_glob": [],
        "vsm_sources_glob": [],
        "sdata_overrides": {},
        "build_dir": "build",
        "output_elf": "build/out.elf",
        "output_map": "build/out.map",
        "preserve_in_build": [],
        "objdiff": {},
        "section_flag_overrides": {},
        "carved_funcs": [],
    }
    raw.update(overrides)
    return cm.Config(raw=raw)


# Canonical 3-function synthetic monolithic.  Mirrors the real shape of
# asm/cod/000000.s: ee-as include, .set directives, .section .text,
# /* ... */ comment + nonmatching marker + glabel/endlabel triplets,
# inter-function pad nop.  Verified shape against the live file.
MONOLITHIC_3FN = textwrap.dedent(
    """\
    .include "macro.inc"

    .set noat
    .set noreorder

    .section .text, "ax"

    /* Generated by spimdisasm 1.40.3 */

    .align 3
    nonmatching alpha, 0x4

    glabel alpha
        /* 0 00100000 00000000 */  nop
    endlabel alpha
        /* 4 00100004 00000000 */  nop

    .align 3
    nonmatching beta, 0x8

    glabel beta
        /* 8 00100008 00000000 */  addiu $sp, $sp, -8
        /* C 0010000C 03E00008 */  jr    $ra
    endlabel beta
        /* 10 00100010 00000000 */  nop

    .align 3
    nonmatching gamma, 0x4

    glabel gamma
        /* 14 00100014 03E00008 */  jr    $ra
    endlabel gamma
    """
)



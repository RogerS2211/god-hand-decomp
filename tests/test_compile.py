class TestIndexFunctions:

    def test_recognises_glabel_endlabel_pairs(self):
        lines = MONOLITHIC_3FN.splitlines(keepends=True)
        idx = cm._index_functions(lines)
        assert set(idx.keys()) == {"alpha", "beta", "gamma"}

    def test_block_start_points_at_align3(self):
        lines = MONOLITHIC_3FN.splitlines(keepends=True)
        idx = cm._index_functions(lines)
        start, glabel, _end = idx["alpha"]
        assert lines[start].strip() == ".align 3"
        assert lines[glabel].strip() == "glabel alpha"

    def test_block_end_swallows_trailing_pad_nop(self):
        """The carve unit extends through inter-function padding to the
        next function's `.align 3` line - 1, so the trailing `nop` after
        `endlabel alpha` is part of alpha's carve, not beta's fragment.
        """
        lines = MONOLITHIC_3FN.splitlines(keepends=True)
        idx = cm._index_functions(lines)
        _start, _glabel, end = idx["alpha"]
        # The carve unit for alpha should include the post-endlabel nop;
        # the next function's `.align 3` lives at end+1.
        assert "nop" in lines[end] or lines[end].strip() == ""
        assert lines[end + 1].strip() == ".align 3"

    def test_block_end_for_last_function_is_eof(self):
        lines = MONOLITHIC_3FN.splitlines(keepends=True)
        idx = cm._index_functions(lines)
        _start, _glabel, end = idx["gamma"]
        # gamma is the last function: its block_end_inclusive is the
        # last index in the source.
        assert end == len(lines) - 1

    def test_nonmatching_marker_inside_carve_unit(self):
        lines = MONOLITHIC_3FN.splitlines(keepends=True)
        idx = cm._index_functions(lines)
        start, _glabel, end = idx["alpha"]
        carve_unit = "".join(lines[start:end + 1])
        assert "nonmatching alpha, 0x4" in carve_unit

    def test_alabel_jlabel_ehlabel_skipped_in_upward_walk(self):
        """alabel/jlabel/ehlabel between `.align 3` and `glabel` must not
        break the upward walk to .align 3.  Real-world example: function
        with leading switch-table alabel sitting between the .align 3 and
        the glabel.
        """
        src = textwrap.dedent("""\
            .section .text, "ax"

            .align 3
            alabel some_aux_sym
            jlabel another_aux
            ehlabel eh_aux
            nonmatching real, 0x4

            glabel real
                nop
            endlabel real
            """)
        lines = src.splitlines(keepends=True)
        idx = cm._index_functions(lines)
        start, _glabel, _end = idx["real"]
        assert lines[start].strip() == ".align 3"

    def test_missing_endlabel_raises(self):
        src = textwrap.dedent("""\
            .section .text, "ax"

            .align 3
            glabel orphan
                nop
            """)
        lines = src.splitlines(keepends=True)
        with pytest.raises(cm.BuildError, match="no endlabel for 'orphan'"):
            cm._index_functions(lines)

    def test_alabel_not_eligible_as_carve_root(self):
        """alabel/jlabel/ehlabel define symbols, but `_index_functions`
        keys off `glabel` matches only — so an `alabel` name will not
        appear in the index and `_split_monolithic` rejects it with
        "function not present in monolithic".
        """
        src = textwrap.dedent("""\
            .section .text, "ax"

            .align 3
            alabel auxiliary_only
            """)
        lines = src.splitlines(keepends=True)
        idx = cm._index_functions(lines)
        assert idx == {}

    def test_no_endlabel_then_orphan_glabel_raises(self):
        """An unterminated function followed by another glabel still
        fails on the missing endlabel of the *first* function.
        """
        src = textwrap.dedent("""\
            .section .text, "ax"

            .align 3
            glabel first
                nop
            .align 3
            glabel second
                nop
            endlabel second
            """)
        lines = src.splitlines(keepends=True)
        with pytest.raises(cm.BuildError, match="no endlabel for 'first'"):
            cm._index_functions(lines)


